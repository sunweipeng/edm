CONTENT_TYPE = {
	"form": "application/x-www-form-urlencoded;charset=utf-8",
	"form-data": "multipart/form-data",
	"json": "application/json;charset=utf-8",
	"xml": "text/xml;charset=utf-8",
	"plain": "text/plain;charset=utf-8",
	"html": "text/html;charset=UTF-8"
}





class BrowserHeaderHelper(object):
	"""
	浏览器USER-AGENT
	"""
	@staticmethod
	def get_user_agent(agent_type=1):
		"""
		获取USER_AGENT
		:param agent_type: 1：PC端 2：手机端 3：全部
		:return:
		"""
		if agent_type == 1:
			user_agent_list = PC_USER_AGENT
		elif agent_type == 2:
			user_agent_list = MOBILE_USER_AGENT
		else:
			user_agent_list = PC_USER_AGENT+MOBILE_USER_AGENT
		"""随机选取"""
		user_agent_item = random.choice(user_agent_list)
		return {"user-agent": user_agent_item}


	@staticmethod
	def get_content_type(content_type='form'):
		"""
		获取content_type
		:param content_type:
		:return:
		"""
		return {"content_type": CONTENT_TYPE.get(content_type, "application/x-www-form-urlencoded;charset=utf-8")}



	@staticmethod
	def get_user_agent_and_content_type(agent_type, content_type):
		"""
		获取用户user_agent and content_type
		:param agent_type:
		:param content_type:
		:return:
		"""
		"""user_agent"""
		user_agent_dict = BrowserHeaderHelper.get_user_agent(agent_type)
		"""content_type"""
		content_type_dict = BrowserHeaderHelper.get_content_type(content_type)
		user_agent_dict.update(content_type_dict)
		return user_agent_dict



